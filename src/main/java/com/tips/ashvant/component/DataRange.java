package com.tips.ashvant.component;

public class DataRange {

    private String from;
    private String to;

    public DataRange() {
    }

    public DataRange(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }
}
